        Home FHome = new Home();
        if(TxtUser.getText().equalsIgnoreCase("Ingresa tu nombre") || TxtUser.getText().equalsIgnoreCase("") || TxtEdad.getText().equalsIgnoreCase("Ingresa tu edad") || TxtEdad.getText().equalsIgnoreCase("")){
            javax.swing.JOptionPane.showMessageDialog(this, "Ingresa los datos correspondientes por favor.", "Datos vacios", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }//if(TxtEdad.getText() != "Ingresa tu edad" || TxtEdad.getText() != ""){
            //javax.swing.JOptionPane.showMessageDialog(this, "Ingresa los datos correspondientes por favor.", "Datos vacios", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        //}
        else{
        FHome.name(TxtUser.getText());
        boolean hombre = RBtnHombre.isSelected();
        boolean mujer = RBtnMujer.isSelected();
        if(hombre){
            FHome.sexo("Sexo: Masculino");
        }else if(mujer){
            FHome.sexo("Sexo: Femenino");
        }else{
            FHome.sexo("Sexo: Anonimo");
        }
        int edad = Integer.parseInt(TxtEdad.getText());
        if(edad <= 90 && edad >= 11){
            FHome.edad(TxtEdad.getText());
            FHome.setVisible(true);
            this.setVisible(false);
        }else{
            javax.swing.JOptionPane.showMessageDialog(this, "Lo siento tu edad parece irreal y/o eres menor de 11 a√±os.", "Edad erronea", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
        
        
        }
