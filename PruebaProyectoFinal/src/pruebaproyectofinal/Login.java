    private void TxtFieldUserMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TxtFieldUserMousePressed
        if (TxtFieldUser.getText().equals("Ingresa tu nombre")) {
            TxtFieldUser.setText("");
            TxtFieldUser.setForeground(Color.black);
        }
        if (TFEdad.getText().equals("")) {
            TFEdad.setText("Ingresa tu edad");
            TFEdad.setForeground(Color.gray);
        }
    }//GEN-LAST:event_TxtFieldUserMousePressed

    private void TFEdadMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TFEdadMousePressed
        if (TFEdad.getText().equals("Ingresa tu edad")) {
            TFEdad.setText("");
            TFEdad.setForeground(Color.black);
        }
        if (TxtFieldUser.getText().isEmpty()) {
            TxtFieldUser.setText("Ingresa tu nombre");
            TxtFieldUser.setForeground(Color.gray);
        }
    }//GEN-LAST:event_TFEdadMousePressed

